public class ScoreDto {
	
	private double iqScore;
	
	private double techScore;
	
	private double hrScore;
	
	private double aveScore;

	public double getIqScore() {
		return iqScore;
	}

	public void setIqScore(double iqScore) {
		this.iqScore = iqScore;
	}

	public double getTechScore() {
		return techScore;
	}

	public void setTechScore(double techScore) {
		this.techScore = techScore;
	}

	public double getHrScore() {
		return hrScore;
	}

	public void setHrScore(double hrScore) {
		this.hrScore = hrScore;
	}

	public double getAveScore() {
		return aveScore;
	}

	public void setAveScore(double aveScore) {
		this.aveScore = aveScore;
	}
	
	

}
